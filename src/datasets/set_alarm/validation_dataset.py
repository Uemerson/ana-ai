"""
This module generates a comprehensive validation dataset
for the 'Set Alarm' intent, covering various patterns,
times, dates, and actions. Each entry includes the text
and corresponding entities for validation an NER model.
"""

TIMES = ["5am", "9:30am", "10 pm", "7:45 am", "11:15 pm"]
DATES = ["today", "tomorrow"]
ACTIONS = [
    "wake up",
    "go to gym",
    "study",
    "meeting",
    "call mom",
    "meditation",
    "take medicine",
    "pick up kids",
]

PATTERNS = [
    # Basic
    "Set an alarm for {time}",
    "Set an alarm for {time} {date}",
    "Set alarm for {time}",
    "Set alarm for {time} {date}",
    # Wake phrases
    "Wake me up at {time}",
    "Wake me up at {time} {date}",
    "Wake me at {time}",
    "Wake me at {time} {date}",
    # With actions
    "I need an alarm at {time} for {action}",
    "Set an alarm for {time} to {action}",
    "Set alarm {time} for {action}",
    "Wake me up at {time} to {action}",
    "Remind me to {action} at {time}",
    "Remind me to {action} at {time} {date}",
    "Set a reminder to {action} at {time}",
    "Set a reminder to {action} at {time} {date}",
    # Question style
    "Can you set an alarm for {time}?",
    "Can you set an alarm for {time} {date}?",
    "Can you set an alarm {date} at {time}?",
    "Could you wake me up at {time}?",
    "Could you wake me up at {time} {date}?",
    # Polite / conversational
    "Please set an alarm for {time}",
    "Please set an alarm for {time} {date}",
    "Hey, set an alarm for {time}",
    "Hey, set an alarm for {time} {date}",
    "Hi, I need an alarm at {time}",
    "Hi, I need an alarm at {time} {date}",
    # Compact / voice style
    "Alarm at {time}",
    "Alarm at {time} {date}",
    "Alarm for {time}",
    "Alarm for {time} {date}",
    "{time} alarm",
    "{time} alarm {date}",
    # Mixed structures
    "Set alarm {time} {date} for {action}",
    "Set an alarm {date} at {time} for {action}",
    "Wake me {date} at {time} to {action}",
    "Remind me {date} at {time} to {action}",
    # Intent-first phrasing
    "I want to wake up at {time}",
    "I want to wake up at {time} {date}",
    "I want an alarm at {time}",
    "I want an alarm at {time} {date}",
    # Slightly noisy / natural
    "Set up an alarm for {time}",
    "Set up an alarm for {time} {date}",
    "Gonna need an alarm at {time}",
    "Gonna need an alarm at {time} {date}",
    # Imperative short commands
    "Set {time} alarm",
    "Set {time} alarm {date}",
    "Set {time} alarm for {action}",
]


def find_entity(text, value, label):
    """
    Helper function to find the start and end
    indices of an entity in the text.
    """
    if not value:
        return None

    start = text.find(value)
    if start != -1:
        return (start, start + len(value), label)
    return None


def clean_text(text: str) -> str:
    """
    Cleans extra spaces generated by optional fields.
    """
    return " ".join(text.split())


def set_alarm_validation_dataset():
    """
    Generates a comprehensive validation dataset
    avoiding duplicated sentences caused by unused variables.
    """

    validation_data = []
    seen = set()

    for pattern in PATTERNS:
        times = TIMES
        dates = DATES if "{date}" in pattern else [None]
        actions = ACTIONS if "{action}" in pattern else [None]

        for time in times:
            for date in dates:
                for action in actions:

                    text = pattern.format(
                        time=time,
                        date=date if date else "",
                        action=action if action else "",
                    )

                    text = clean_text(text)

                    if text in seen:
                        continue
                    seen.add(text)

                    entities = []

                    time_ent = find_entity(text, time, "TIME")
                    if time_ent:
                        entities.append(time_ent)

                    if date:
                        date_ent = find_entity(text, date, "DATE")
                        if date_ent:
                            entities.append(date_ent)

                    validation_data.append(
                        (
                            text,
                            {"cats": {"SET_ALARM": 1.0}, "entities": entities},
                        )
                    )

    return validation_data


if __name__ == "__main__":
    # Generate dataset
    VALIDATION_DATA = set_alarm_validation_dataset()

    # Preview
    for item in VALIDATION_DATA[:50]:
        print(item)

    print(f"\nTotal samples: {len(VALIDATION_DATA)}")
